import matplotlib.pyplot as plt
import numpy as np
x1 = np.array([5,10,15,20,25,30,60])
y1 = np.array([50,100,80,90,150,180,120])
plt.figure(figsize=(8,8))
plt.xlabel('This is X')
plt.ylabel('This is Y')
plt.subplot(1,2,1)
plt.grid(axis='x',color='black',ls='--')
plt.plot(x1,y1,marker='|',ms=15,color='green',mec='yellow')
x1 = np.array([2,5,10,6,8])
y1 = np.array([20,50,40,80,90])
plt.xlabel('This is X')
plt.ylabel('This is Y')
plt.subplot(1,2,2)
plt.grid(axis='y',color='hotpink',ls=':')
plt.plot(x1,y1,marker='|',ms=25,color='green',mec='red',ls='-.')
plt.suptitle('This is Trial')
plt.show()